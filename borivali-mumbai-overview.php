            <p>The leading hospitals in Borivali and its environs comprise Rajendra Maternity and Surgical Nursing Home, Ganatra&#39;s Riddhi Nursing Home, Neoplus Criticare Children Hospital, Karuna Hospital, Navneet Hospital, Apex Superspeciality Hospitals and Lotus Multispeciality Hospital. There are also some educational institutions including Rustomjee Cambridge International School, Himalaya High School, K.E.S Shroff College, Shri T.P. Bhatia College, Don Bosco High School Swami Vivekanand International School and St. Xaviers High School. The people living here have easy access to such dynamic centres of commerce as Charkop Market, VMall Orlem Market Maxus Cinema PVR Milap.</p>



