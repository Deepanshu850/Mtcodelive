  <script>
    const ulIds = ['noida-sectors list-2', 'gurgaon-sectors list-3']; // Replace with your actual array of IDs

    ulIds.forEach(ulId => {
      const ulElement = document.getElementById(ulId); // Select the corresponding UL
      if (ulElement) {
        const listItems = Array.from(ulElement.querySelectorAll('li')); // Get all LI elements

        // Sort the list items based on the numeric value found in their text content
        listItems.sort((a, b) => {
          const aMatch = a.textContent.match(/sector (\d+)/i);
          const bMatch = b.textContent.match(/sector (\d+)/i);

          const aNum = aMatch ? parseInt(aMatch[1], 10) : Infinity; // Default to Infinity if no match
          const bNum = bMatch ? parseInt(bMatch[1], 10) : Infinity;

          return aNum - bNum; // Sort in ascending order
        });

        // Clear the existing list items from the UL
        ulElement.innerHTML = '';

        // Append the sorted list items back to the UL
        listItems.forEach(item => {
          ulElement.appendChild(item);
        });
      }
    });
  </script>


