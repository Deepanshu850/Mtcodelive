  <script>
    document.addEventListener("DOMContentLoaded", function() {
      const headers = document.querySelectorAll("#header-container h2");
      const lists = document.querySelectorAll("#list-container ul");
      const searchInput = document.getElementById("search");
      const filteredListContainer = document.getElementById("filtered-list");

      // Function to clear filtered list and show all lists
      const clearAndShowLists = () => {
        lists.forEach(list => list.classList.remove("hidden"));
        filteredListContainer.innerHTML = ''; // Clear previous search results
      };


      headers.forEach((header, index) => {
        header.addEventListener("click", () => {
          clearAndShowLists(); // Show all lists when a header is clicked
          lists.forEach(list => list.classList.add("hidden")); // Hide all lists
          lists[index].classList.remove("hidden"); // Show the clicked section's list

          // Remove active class from all h2 elements inside #header-container
          headers.forEach(h => {
            h.classList.remove('active'); // Remove active class
          });

          // Add active class to the clicked header
          header.classList.add('active'); // Add active class to apply styles
        });
      });


      // Filter functionality
      searchInput.addEventListener("input", () => {
        const searchTerm = searchInput.value.toLowerCase();
        filteredListContainer.innerHTML = ''; // Clear previous results
        let found = false; // Track if any matches are found

        lists.forEach(list => {
          const items = list.querySelectorAll('li');

          items.forEach(item => {
            if (item.textContent.toLowerCase().includes(searchTerm)) {
              const newListItem = document.createElement('div');
              newListItem.classList.add('match-item');
              newListItem.innerHTML = item.innerHTML;

              filteredListContainer.appendChild(newListItem);
              found = true;
            }
          });
        });

        // Hide all lists when searching
        lists.forEach(list => list.classList.add("hidden"));

        // Show 'no results' message if no items match
        if (!found) {
          const noResults = document.createElement('div');
          noResults.textContent = 'No results found.';
          noResults.classList.add('no-results');
          filteredListContainer.appendChild(noResults);
        }
      });
    });
  </script>


