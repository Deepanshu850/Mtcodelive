    $("#contact").submit(function(event) {
        event.preventDefault(); // Prevent the default form submission

        var formData = {
            name: $("#name").val(),
            number: $("#number").val(),
            subject: $("#subject").val(),
            message: $("#message").val()
        };

        $.ajax({
            type: "POST",
            url: "submit_contact.php", // URL to the PHP file that processes the form
            data: formData,
            dataType: "json",
            encode: true,
        })
        .done(function(data) {
            if (data.success) {
                alert('Success: ' + data.message);
            } else {
                alert('Success: ' + data.message);
            }
        })
        .fail(function() {
            alert('There was an error with the submission. Please try again later.');
        });
    });
});
