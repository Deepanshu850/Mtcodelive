                    <h2 id=heading-1>
                        UP RERA - An Overview
                    </h2>
                    <p>
                        <strong>UP RERA</strong> is the <strong>state regulatory authority</strong> which is responsible for <strong>streamlining real estate business practices</strong> across the state of Uttar Pradesh and maintaining transparency, integrity, and accountability in the property buying and selling process. The <a href=./understanding-rera-act-2016.php><strong>UP RERA Act 2016</strong></a> came into existence on and from <strong>27 October, 2016</strong> and consists of 10 chapters which are further classified into 92 provisions, bringing uniformity and standardised professionalism in the <strong>real estate sector</strong>, for buyers and builders across the state.
                        <br><br>
                        UP RERA works under the leadership of its board members and the chairman, <strong>Mr. Sanjay R. Bhoosreddy</strong>.
                    </p>
                    <h3 id=subheading-1-1>
                        UP RERA Full Form and its Analysis
                    </h3>
                    <p>
                        UP RERA is the acronym for <strong>Uttar Pradesh Real Estate Regulatory Authority</strong>, which came into existence to bring regulation and standardised practices in the blooming real estate sector. UP RERA upholds the rights and interests of buyers and builders and provides a fast-track mechanism for <strong>dispute resolution</strong>.
                    </p>
                    <h3 id=subheading-1-2>
                        Need for a Real Estate Regulatory Authority in UP
                    </h3>
                    <p>
                        The real estate industry of Uttar Pradesh is blooming with state-of-the-art infrastructure featuring top-notch <strong><a href=../category/commercial-property.php>commercial</a> and <a href=../category/residential-property.php>residential properties</a></strong> from leading real estate builders in the country.
                        <br><br>
                        Although it was widely unregulated. <a href=./understanding-rera-act-2016.php><strong>UP RERA</strong></a> brings uniform rules and regulation and brings trust and confidence from buyers and real estate investors in the sector.
                    </p>
                    <h3 id=subheading-1-3>
                        What are the benefits of UP RERA?
                    </h3>
                    <p>
                        UP RERA changed the dynamics of the real estate sector by bringing <strong>revolutionary changes</strong> and streamlining the real estate business. The key benefits of UP RERA include:
                    </p>
                    <ul class=arrow-list>
                        <li>
                            Brings <strong>transparency</strong> to real estate transactions
                        </li>
                        <li>
                            Introduce <strong>standardised</strong> and streamlined practices in the sector
                        </li>
                        <li>
                            Infusing <strong>professionalism</strong> into the real estate industry
                        </li>
                        <li>
                            Ensure <strong>accountability</strong> and integrity for buyers and builders
                        </li>
                        <li>
                            Introduce a <strong>fast-track</strong> mechanism for dispute resolution
                        </li>
                        <li>
                            Boost the <strong>confidence</strong> of investors in the real estate industry with a regulatory body
                        </li>
                    </ul>
                    <h3 id=subheading-1-4>
                        Roles of UP RERA
                    </h3>
                    <p>
                        The <strong>RERA Act 2016</strong> mandates the regulation of <strong>commercial and residential properties</strong> under the state regulatory authority. <strong>UP RERA</strong> governs the regulation of all types of real estate and instructs builders/promoters to register their upcoming projects <strong>exceeding 500 sq metres, or more than 8 apartments</strong> with the regulatory. It also mandates builders and <a href=../top-channel-partner-in-real-estate.php><strong>channel partners</strong></a> to bear the <strong>unique project-wise RERA registration number</strong> while marketing and selling their real estate project.
                    </p>
                    <h4>
                        The roles of UP RERA include:
                    </h4>
                    <ul class=arrow-list>
                        <li>
                            To bring regulation and promotion to the real estate sector
                        </li>
                        <li>
                            To ensure transparency and quality in real estate projects
                        </li>
                        <li>
                            To address the rights and interests of buyers and builders
                        </li>
                        <li>
                            To enlist builders in providing original and accurate information regarding real project
                        </li>
                    </ul>
                    <h3 id=subheading-1-5>
                        Impact of UP RERA on the Real Estate Sector
                    </h3>
                    <p>
                        UP RERA has changed the dynamics of the real estate sector in India by bringing standard professionalism across all real estate projects in the country. The RERA Act 2016 enhances the <strong>quality of upcoming projects</strong> and ensures their <strong>timely delivery</strong> taking into consideration the <strong>rights and interests</strong> of both buyers and builders.
                    </p>
                    <h4>
                        The crucial impacts of the UP RERA Act are:
                    </h4>
                    <ul class=arrow-list>
                        <li>
                            UP RERA mandates all builders to bear the unique project-wise <strong>RERA registration</strong> during the marketing and selling of their real estate projects for authenticity.
                        </li>
                        <li>
                            UP RERA restricts promoters/builders to make changes to the sanctioned plan without the prior consent of ⅔ of the buyers.
                        </li>
                        <li>
                            The state regulatory authority establishes a fast-track portal for grievance redress and dispute resolution.
                        </li>
                        <li>
                            UP RERA mandates promoters/builders to rectify any issue raised by the buyer within the first 5 years of project delivery.
                        </li>
                    </ul>
                    <h3 id=subheading-1-6>
                        Defining Carpet Area under UP RERA Act
                    </h3>
                    <p>
                        UP RERA instructed promoters to market and sell their real estate projects based on the <strong>standardised ‘carpet area’</strong> and not the ‘super built-up area’. The <strong>RERA Act 2016</strong> set the standard definition of ‘carpet area’ across all real estate projects like apartments, retail shops, plots, malls, multiplexes, and buildings in the country and enhanced the property buying and selling experience.
                    </p>
                    <h4>
                        Definition of Carpet Area under the RERA Act 2016:
                    </h4>
                    <p>
                        <strong>‘Carpet Area’</strong> refers to the net usable area of an apartment in a real estate project, excluding the area covered under external walls, service shafts, exclusive balcony/veranda area, and exclusive open terrace area. The ‘carpet area’ includes the area covered under the inner walls of partitions.
                        <br><br>
                        Setting the standard definition of ‘carpet area’, including others like ‘advertisement’, ‘agreement for sale’, ‘building’, ‘commencement certificate’, ‘completion certificate’, ‘occupancy certificate’ etc, enables <strong>clear transparency</strong> and <strong>authenticity</strong> in real estate transactions.
                    </p>
                    <h2 id=heading-2>
                        Rules and Regulations in UP RERA
                    </h2>
                    <p>
                        The key rules and regulations in UP RERA include:
                    </p>
                    <ul class=arrow-list>
                        <li>
                            A real estate project exceeding more than 500 sq metres, or more than 8 floors is required to bear a unique RERA registration number after submitting original documents regarding the project's sanctioned plan, and authenticated certificate.
                        </li>
                        <li>
                            UP RERA Act mandates promoters/builders to deposit 70% of investors’ and buyers’ money in a discreet bank account which would then be allotted for property-related constructions.
                        </li>
                        <li>
                            It enables the right of the consumer to ask for compensation from the promoter in case of any discrepancy in the real estate project delivered.
                        </li>
                    </ul>
