                    <h2 id=heading-2>
                        Key Points Under the RERA Act, 2016
                    </h2>
                    <p>
                        <strong>Real Estate (Regulation and Development) Act, 2016</strong> brings uniformity in real estate practices across the country and assures a <strong>transparent and seamless experience for buyers and builders</strong> in the process of buying and selling real estate projects.
                    </p>
                    <h3 id=subheading-2-1>
                        Crucial key points under the RERA Act, 2016 include:
                    </h3>
                    <ul class=arrow-list>
                        <li>
                            <h3>
                                Enhanced Security:
                            </h3>
                            <p>
                                The provisions under the RERA Act instruct to keep 70% of the investors’ and buyers’ money in a separate account, which would then be allotted to the builder for construction and land related costs. It restricts builders not to ask more than 10% of the property’s cost as an advance payment before signing the sale agreement.
                            </p>
                        </li>
                        <li>
                            <h3>
                                Clear Transparency:
                            </h3>
                            <p>
                                While applying for RERA registration, builders are required to submit original documents for their real estate projects and undertakings. The RERA Act restricts builders not to make changes to the property plans, without the prior consent of the buyers.
                            </p>
                        </li>
                        <li>
                            <h3>
                                Unbiased Fairness:
                            </h3>
                            <p>
                                The RERA Act laid out definitions of important terms like ‘Carpet Area’, 'Common Areas’, ‘Apartment’, ‘Building’ etc and brought in uniformity and fairness in real estate business practices. It instructed builders to sell and promote properties based on the ‘carpet area’ and not the ‘super built-up area’. It titled investors to get the entire money invested, in case a project has been delayed.
                            </p>
                        </li>
                        <li>
                            <h3>
                                Superior Quality:
                            </h3>
                            <p>
                                The provisions in the RERA Act mandated builders to rectify and make amends to the issues and challenges faced by the buyer in the first 5 years of purchase. It instructed builders to rectify the issues raised within 30 days of the complaint.
                            </p>
                        </li>
                        <li>
                            <h3>
                                Reliable Authorisation:
                            </h3>
                            <p>
                                The provisions in the REAR act restrict regulators not to market, sell, build, invest, or book a plot without registering it with the state’s RERA. It mandates builders and promoters/channel partners to bear the unique project-wise REAR registration number while marketing and selling real estate projects.
                            </p>
                        </li>
                    </ul>
                    <h3 id=subheading-2-2>
                        How do I make sure that a property is RERA compliant?
                    </h3>
                    <p>
                        The main points to consider before understanding if a property is RERA Compliant are:
                    </p>
                    <ul class=arrow-list>
                        <li>
                            If the area of the property is <strong>more than 500 sq metres</strong>, the builder must register it under the state real estate regulatory.
                        </li>
                        <li>
                            The builder must <strong>provide the original documents</strong> to get approval from the RERA, containing information regarding the <strong>property sanctioned plan</strong> and its brief details, <strong>enterprises’ brief details</strong>, and <strong>authenticated certificates</strong>.
                        </li>
                        <li>
                            RERA Act instructed builders to provide proof that <strong>70% of investors’ and buyers’ money is deposited in a discreet bank account</strong> and could only be used for construction and land-related purposes.
                        </li>
                        <li>
                            Builders are mandated to obtain a <strong>unique project-wise RERA registration number</strong> before marketing and selling their real estate projects.
                        </li>
                    </ul>
                    <h2 id=heading-3>
                        How does the RERA Act benefit Real Estate Business?
                    </h2>
                    <p>
                        According to the <a href=https://www.ibef.org/industry/real-estate-india><strong>IBEF Report</strong></a>, the <a href=./best-city-to-invest-in-real-estate-in-india.php><strong>real estate industry in India</strong></a>, which is about to be around Rs. 12,000 Cr (US$ 1.72 billion) in the financial year 2019, is estimated to grow to Rs. 65,000 Cr. (US$ 9.30 billion). The report further estimated that the real estate market size in India will reach <strong>$1 trillion by 2030</strong>.
                        <br><br>
                        <strong> RERA Act, 2016</strong> brought about revolutionary changes in the real estate industry in India by channelising it and streamlining the responsibilities and liabilities of buyers and builders which consequently lead to a seamless buying and selling experience in the real estate business.
                    </p>
                    <h3 id=subheading-3-1>
                        Key benefits of the RERA Act include:
                    </h3>
                    <ul class=arrow-list>
                        <li>
                            <h3>
                                Consistent Carpet Area:
                            </h3>
                            <p>
                                The provisions in the RERA Act set the standard definition of ‘Carpet Area’ across every real estate project. It restricted promoters to asking for increased prices based on inflated carpet areas.
                            </p>
                        </li>
                        <li>
                            <h3>
                                Builder’s Security against Insolvency:
                            </h3>
                            <p>
                                RERA Act instructed builders to maintain a discreet account that contains 70% of the investors’ and buyers’ money and could only be used for construction and land-related of that particular project. Unlike earlier, builders are not allowed to move funds collected from one real estate project to another.
                            </p>
                        </li>
                        <li>
                            <h3>
                                Upfront Payment Clarity:
                            </h3>
                            <p>
                                The provisions in the RERA Act restrict builders to asking for more than 10% of the property value before signing the sale agreement. It gives relaxation to buyers from having to fund huge amounts of money while making a real estate investment.
                            </p>
                        </li>
                        <li>
                            <h3>
                                Protection against any Defect:
                            </h3>
                            <p>
                                The RERA Act instructs builders to rectify issues faced by buyers in the first 5 years of possession of the property.
                            </p>
                        </li>
                        <li>
                            <h3>
                                Interest Compensation:
                            </h3>
                            <p>
                                The provisions in the RERA Act enable the same amount of interest to be paid by both the parties, buyers and builders, in case of delayed payment and delayed possession, respectively.
                            </p>
                        </li>
