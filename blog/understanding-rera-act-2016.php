                    <h2 id=heading-1>
                        Real Estate Regulation and Development Act, 2016 - An Overview
                    </h2>
                    <p>
                        The Indian Parliament passed the <strong>Real Estate (Regulation and Development) Act 2016</strong> to streamline the blooming real estate industry and make the property buying and selling process smooth, transparent and integral. The purpose of the <strong>RERA Act</strong> is to uphold the rights and liabilities of buyers and builders across all states of the country. The RERA Act is effective on and from <strong>1 May, 2016</strong> and comprises 10 chapters which are further classified into 92 provisions, of which 52 were notified at that time and the rest were effective on and from 1 May, 2017.
                    </p>
                    <h3 id=subheading-1-1>
                        Need for Regulatory in the Real Estate Sector
                    </h3>
                    <p>
                        The <strong>Real Estate Sector in India</strong> is growing at a significant pace and emerging as a prominent source of wealth creation for people looking to invest their money with a goal of high rate of investment. Currently, the real estate industry in India contributes about <strong>8.53% of total GDP</strong>.
                        <br><br>
                        With a <strong>growth rate of about 30%</strong>, India’s real estate market has witnessed emerging state-of-the-art commercial and residential properties from the most sought-after builders across the nation like <strong>Godrej Properties, L&T Realty, M3M Group, DLF Limited, ACE Group etc</strong>.
                        <br><br>
                        RERA Act drafted out a <strong>proper policy and regulation</strong> for this blooming real estate sector and to enhance <a href=./real-estate-investment-a-comprehensive-guide-on-real-estate.php><strong>real estate investments</strong></a> that prompt a large group of people to <a href=../property-in-noida.php><strong>buy property for sale</strong></a> under a regulation policy that protects their rights and interests.
                    </p>
                    <h3 id=subheading-1-2>
                        What is the RERA Act?
                    </h3>
                    <p>
                        Real estate sector in India has been the <strong>crucial pillar of the economy</strong> and is the source of wealth creation and earning for a large population of India. Although, it was widely unregulated.
                        <br>
                        In order to regulate the real estate sector and <strong>bring professionalism and standardisation across the country</strong>, the parliament enacted <strong>RERA Act</strong>, effective on and from 1 May, 2016.
                        <br><br>
                        <strong>RERA Act</strong> is a real estate regulation policy containing <strong>92 provisions</strong> which aims to uphold the consumer rights and interest of the buyers and <strong>brings uniformity and standardised business practises</strong> across every real estate project in the country. It set transparency, integrity, and accountability in the <a href=./understanding-real-estate-business.php><strong>real estate business</strong></a> and enabled a <strong>fast-track mechanism for the resolution of disputes</strong>.
                    </p>
                    <h3 id=subheading-1-3>
                        RERA Full Form and its Analysis
                    </h3>
                    <p>
                        <strong>RERA stands for Real Estate Regulatory Authority</strong>, a regulatory body that came with the enactment of <strong>RERA Act, 2016</strong> and protects the rights and interests of buyers and builders by bringing uniform and standard practices in the real estate sector across the country. The Real Estate Regulatory Authority regulates both <strong>commercial and residential properties</strong> in accordance with the laws presented in the <strong>Real Estate (Regulation and Development) Act, 2016</strong>.
                    </p>
                    <h3>
                        Objectives of the RERA Act, 2016 are:
                    </h3>
                    <ul class=arrow-list>
                        <li>Bringing transparency</li>
                        <li>Introducing uniform and standardised real estate practices</li>
                        <li>Infusing professionalism in the real estate business across the country</li>
                        <li>Ensuring accountability and integrity</li>
                        <li>Allowing a symmetrical flow of information between buyers and builders</li>
                        <li>Establishing a regulatory body to oversee a seamless mechanism</li>
                        <li>Introducing a fast-track mechanism for disputes resolution</li>
                        <li>Bringing confidence to the sector and prompting people to buy property for sale.</li>
                    </ul>
