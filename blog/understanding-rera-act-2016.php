                    <h3 id=subheading-1-4>
                        What is RERA Approved?
                    </h3>
                    <p>
                        <strong>RERA approved</strong> is a layman term for a real estate project that has acquired the mandatory papers and approval from the state real estate regulatory to build a property for sale.
                        <br>
                        <br>
                        The term ‘RERA Approved’ is often used by builders and <strong>channel partners</strong> in real estate in their <strong>sales and marketing strategies</strong>.
                        <strong>RERA registered</strong> or <strong>RERA Certified</strong> is an official term used to denote a property for sale that is registered under the state’s real estate regulatory authority. After the enactment of the <strong>RERA Act, 2016</strong>, each builder with an area of property more than 500 sq m, needs to register that property under the state's RERA before starting marketing the real estate project.
                        <br><br>
                        <strong>‘RERA approved’ and ‘RERA registered’</strong> are often used interchangeably in day-to-day usage but in fact they are different terms. It is advised to buyers to do due diligence and analyse the project details on the official RERA website before making a real estate investment.
                    </p>
