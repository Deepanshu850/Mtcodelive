                                Safeguards against False Promises:
                            </h3>
                            <p>
                                The RERA Act restricts builders to make changes to real estate project sanctioned plans before the prior consent of the buyers. In case of any discrepancies in the project built and what was promised, the law entitles buyers to a full repayment of the advance money paid.
                            </p>
                        </li>
                        <li>
                            <h3>
                                Compensation Rights:
                            </h3>
                            <p>
                                If the buyer finds any discrepancy in the title of the property after taking its possessions, the RERA Act enables the buyer to claim for compensation from the promoters/builders.
                            </p>
                        </li>
                        <li>
                            <h3>
                                Right to Information:
                            </h3>
                            <p>
                                The RERA Act mandates the right of buyers to know all the information regarding a particular project such as its design layout and plans, completion status etc in accordance with the RTI Act.
                            </p>
                        </li>
                        <li>
                            <h3>
                                Easy Grievance Redressal:
                            </h3>
                            <p>
                                The RERA Act enables buyers, agents, and promoters to file complaints against a particular real estate project to RERA. The RERA Act brings in a fast-track and streamlined mechanism for dispute resolution.
                            </p>
                        </li>
                    </ul>
