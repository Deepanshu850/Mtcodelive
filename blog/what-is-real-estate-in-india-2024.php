                    <h2>
                        What is RERA in Real Estate?
                    </h2>


                    <p>
                        The Indian Parliament in 2016 passed the <strong>Real Estate (Regulation & Development)
                            Act, 2016</strong> which led to the foundation of the <strong>Real Estate Regulatory Authority
                            (RERA)</strong> responsible for maintaining transparency in the Indian Real Estate Market and
                        resolving the existing discrepancies and various other issues within it.
                        <br><br>
                        The Act gives provision to States/UTs to list out various specific rules and regulations to
                        monitor the well-functioning of <strong>transparency between the real estate developer
                            and the client.</strong>



