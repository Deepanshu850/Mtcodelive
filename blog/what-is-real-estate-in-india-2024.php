                    <p>Many of the <strong>leading real estate developers in India</strong> have started taking measures to minimise the effects and getting <strong>LEED certified </strong>like Supernova in Noida, CII-Sohrabji Godrej Green Business in Hyderabad, Suzlon One Earth in Pune, and Rajiv Gandhi International Airport - one of the <a href="./biggest-airports-in-india.php"><strong>largest airport in India</strong></a>.</p>

