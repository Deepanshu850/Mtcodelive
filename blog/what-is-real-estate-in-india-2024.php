                        What is Real Estate?

                    </h2>
                    <p>
                        <strong>Real estate</strong> is a form of real property that consists of buildings or homes permanently
                        attached to land. The term 'real estate' means real (or physical) property.
                        <br><br>
                        The <strong>real estate sector in India</strong> is blooming at an exponential rate currently valued at
                        $477 billion, just second to the agricultural sector, and contributes 7.3% to the
                        country’s GDP.


                    </p>


