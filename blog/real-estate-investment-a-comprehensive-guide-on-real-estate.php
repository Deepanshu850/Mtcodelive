                        various discrepancies in the <strong>real estate industry in India</strong>.
                    </p>
                    <p>
                        Every state in India has its own body of Real Estate Regulatory Authority which
                        regulates a complete list of properties and real estate investment in India. You can
                        check complete details regarding any property for sale on the RERA website.
