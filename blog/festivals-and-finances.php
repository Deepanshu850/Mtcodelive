                    <h3>How to calculate housing loan interest rate? </h3>

                    <p>Homeowners can use the following calculation to determine their EMIs:</p>

                    <p style="text-align: center;"> <strong> EMI = [P x R x (1+R)^N]/[(1+R)^N-1]</strong></p>

                    <p style="text-align: center;">Where,</p>

                    <p style="text-align: center;">P = Principal loan amount.</p>

                    <p style="text-align: center;">R = Monthly interest rate.</p>

                    <p style="text-align: center;">N = Loan tenure in months.</p>

