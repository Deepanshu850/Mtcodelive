                    <h3>GST on Commercial Property in Noida</h3>

                    <p>GST stands for <strong>Goods and Services Tax. </strong>It is a multi-stage tax, (Added at every stage from production to its sale), and charged where the item is actually used, making it destination-based, removing multiple kinds of taxes, and making it a unified tax system across India. </p>

                    <p>GST is imposed on the <strong>supply of goods and services within India </strong></p>

                    <p><strong>The Goods and Services Tax (GST)</strong> on commercial property can vary depending on the specific kind of transaction and associated services. </p>

                    <p>On selling a commercial property, the GST rate is usually 5 percent, while the rent of a commercial property is usually 18 percent. </p>

                    <p>In order to calculate GST we have to know the <strong>base price of the goods</strong> and services and the applicable GST rate, which is decided by the government. </p>

                    <p>The GST amount will be the product of the base price and GST rate, the the total price will be the <strong>product of the base price and GST amount. </strong></p>

                    <p>To put it in a formula:</p>

                    <p><strong>GST Amount = Base Price * GST Rate</strong></p>

                    <p><strong>Total Price = Base Price + GST Amount</strong></p>

