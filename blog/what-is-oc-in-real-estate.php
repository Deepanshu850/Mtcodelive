                    <h2 id="heading-4">Debunking common misconceptions on OC in Real Estate! </h2>

                    <h3>Misconception 1: Occupancy Certificate is same as a Completion Certificate</h3>

                    <p><strong>Reality:</strong> An OC certifies that a building is constructed in compliance with all the safety norms and regulations after thorough site inspection from a team of professionals, whereas a CC certifies that a building is completed in compliance with all the safety norms and regulations.</p>

                    <h3>Misconception 2: Occupancy Certificate is required for just large projects</h3>

                    <p><strong>Reality: </strong>An OC is required for all types of real estate projects, be it commercial property or residential property of any size and area, irrespective of its location.</p>

                    <h3>Misconception 3: Occupancy Certificate is optional if possession is given</h3>

                    <p><strong>Reality: </strong>Possession of a property without an OC is illegal and the Real Estate Regulatory Authority can initiate legal action against the real estate builder for not obtaining an OC.</p>

                    <h3>Misconception 4: OC is not necessary for home loans or resale</h3>

                    <p><strong>Reality:</strong> Every bank and financial institution requires an OC before approving a home loan as it reduces chances of risks involved and also upscale property&rsquo;s resale value.</p>

                    <h3>Misconception 5: Occupancy Certificate can obtained after moving in</h3>

                    <p><strong>Reality: </strong>Moving in a property (building) without obtaining an OC could be risky as the building has not been inspected for safety measurements and could be prone to disasters. </p>

