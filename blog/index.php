    <script>
        let currentPage = 1;
        const postsPerPage = 5;
        const totalPosts = <?= count($posts) ?>;
        const totalPages = Math.ceil(totalPosts / postsPerPage);
        const posts = <?= json_encode($posts) ?>;
        let filteredPosts = posts;

        function renderPosts(postsToRender) {
            const start = (currentPage - 1) * postsPerPage;
            const end = start + postsPerPage;
            const currentPosts = postsToRender.slice(start, end);

            const blogPostsContainer = document.getElementById('blog-posts-container');
            blogPostsContainer.innerHTML = '';

            currentPosts.forEach(post => {
                const postElement = document.createElement('div');
                postElement.className = 'news-card2 mb-50';
                postElement.innerHTML = `
                <div class="news-img">
                    <a href="${generateSlug(post.title)}"><img src="${post.imageUrl}" alt="${post.title}"></a>
                </div>
                <div class="content">
                    <h4><a href="${generateSlug(post.title)}">${post.title}</a></h4>
                    <p>${post.description}</p>
                    <div class="news-btm d-flex align-items-center justify-content-between">
                        <div class="author-area">
                            <div class="author-content">
                                <h6>${post.writer}</h6>
                                <p style="font-size:12px;margin:0;">Posted on - ${post.date}</p>
                            </div>
                        </div>
                        <a target="_blank" class="view-btn" href="${generateSlug(post.title)}">Read More</a>
                    </div>
                </div>
            `;
                blogPostsContainer.appendChild(postElement);
            });

            document.getElementById('prev-page').disabled = currentPage === 1;
            document.getElementById('next-page').disabled = currentPage === Math.ceil(postsToRender.length / postsPerPage);

            const pageNumbers = document.getElementById('page-numbers');
            pageNumbers.innerHTML = '';
            for (let i = 1; i <= Math.ceil(postsToRender.length / postsPerPage); i++) {
                const pageButton = document.createElement('button');
                pageButton.textContent = i;
                pageButton.className = i === currentPage ? 'active' : '';
                pageButton.onclick = () => navigatePage(i);
                pageNumbers.appendChild(pageButton);
            }
        }

        function navigatePage(page) {
            currentPage = page;
            renderPosts(filteredPosts);
            window.scrollTo(0, 0);
        }

        document.getElementById('prev-page').onclick = () => navigatePage(currentPage - 1);
        document.getElementById('next-page').onclick = () => navigatePage(currentPage + 1);

        document.getElementById('search-input').addEventListener('input', function() {
            const query = this.value.toLowerCase();
            filteredPosts = posts.filter(post => post.title.toLowerCase().includes(query) || post.description.toLowerCase().includes(query));
            currentPage = 1;
            renderPosts(filteredPosts);
        });

        function generateSlug(title) {
            return title.toLowerCase().trim().replace(/[^a-z0-9-]+/g, '-');
        }

        renderPosts(posts);
    </script>





