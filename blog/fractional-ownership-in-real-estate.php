                    <section class="questions">
                        <h2>Who is able to invest in Fractional Ownership?</h2>
                        <p>Any resident or non-resident Indian with an INR denominated bank account and proper KYC can invest in fractional ownership by investing in the SPV (i.e., by owning the Equity Shares and the CCDs of the SPV). NRIs can invest in the SPVs through their NRE / NRO accounts. However, coupon payable, the yields payable, and any other amounts payable to the NRIs by the SPVs will have to be paid to the bank account of the NRIs in India and can be repatriated outside of India as per applicable laws.</p>
