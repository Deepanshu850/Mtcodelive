                    <h2 id="heading-7">FAQs on Fractional Ownership in Real Estate </h2>

                    <section class="questions">
                        <h2>What kind of investments does Fractional Ownership cover?</h2>
                        <p>One can invest through the SPV in an under-construction, ready, or rented property that owns the property. The income, expenses, and profits from the property are shared by multiple fractional owners, managed by the Custodian and the Asset Manager, who runs the SPV on a daily basis for an annual fee.</p>
