                    <h2 id="heading-1">Fractional Ownership in Real Estate - Overview </h2>

                    <p>According to the reports published by JLL India, the market of Fractional Ownership in Real Estate is estimated to touch $5 billion by 2030 in market share and increase by tenth-fold of current valuation of around $500 million. Fractional Ownership in Real Estate is the emerging real estate investment in India backed-up by Real Estate Regulatory Authority (RERA), and SEBI for fair property selling and buying process and enhanced management of portfolio of shares in real estate.</p>

                    <p>Fractional Ownership in Real Estate is a cost-effective property investment that allows you to buy and manage property in shares under the Demat Account. Let&rsquo;s understand fractional ownership in real estate and get better insights for a strategic investment.</p>

                    <h3>What is Fractional Ownership in Real Estate? </h3>

                    <p>Fractional Ownership in Real Estate refers to buying property for sale in shares under the share ownership model in which multiple individuals collectively own a part of a high-end property and earn a part of its rental yields and ROI. With fractional ownership in real estate, you can purchase a high-end commercial or residential in partnerships of other real estate investors on its price appreciation.</p>

                    <p>Traditionally you would be required to make an upfront payment while making a <a href="./real-estate-investment-a-comprehensive-guide-on-real-estate.php"><strong>real estate investment</strong></a> and tends to have higher risk of loss but with fractional ownership in real estate, backed by RERA and SEBI, you can buy property at much lower rates along with minimal chances of low returns in future.</p>

                    <h2 id="heading-2">How Fractional Ownership in Real Estate works? </h2>

                    <p>Fractional Ownership in Real Estate would work under the rules and regulations of Real Estate Regulatory Authority (RERA) and Securities and Exchange Board of India (SEBI), making it a secured and promising investment. The Catalyst Trusteeship Limited would be acting as the trustee of SEBI and would for property valuations, rental expenses, division of property shares, and maintaining the same on Demat Account. </p>

                    <h3>SEBI&rsquo;s Role in Fractional Ownership in Real Estate</h3>

                    <p>Security and Exchange Board of India was established in 1992 and is responsible for managing and overseeing securities and investments in India. The fractional ownership in real estate would be under the scrutiny of SEBI and the authority is responsible to draft rules and regulation much like it did for Real Estate Investment Trusts (REITs) and Infrastructure Investment Trusts (InvITs).</p>

                    <h3>RERA&rsquo;s Role in Fractional Ownership in Real Estate</h3>

                    <p>The RERA Act was passed by the Parliament of India and mandates all the real estate projects to be registered with the authority and bear a unique RERA registration number while marketing and selling of property for sale. The standardised rules and regulation laid out in the Act would also be applicable to fractional ownership in real estate.</p>

