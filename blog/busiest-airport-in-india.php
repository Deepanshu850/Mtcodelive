                    <h2 id="subheading-3-1">Indira Gandhi International Airport</h2>

                    <p><strong>New Delhi, 110037</strong></p>


                    <div class="image-wrapper">
                        <img src="./assets/indira-gandhi-international-airport.webp" alt="Indira Gandhi International Airport">
                    </div>



                    <p>Indira Gandhi International Airport is the busiest airport in India and Asia by passenger traffic handling about 6.55 Cr. (65.5 million) passengers annually. It serves the capital of India, Delhi, and other nearby regions, commonly referred to as the National Capital Region (NCR). IGI Airport consists of 3 terminals for domestic and international travel along with a cargo terminal. The terminal 3 of Delhi Airport was recently inaugurated and boosts the capacity to handle 3.4 Crores (34 million) passengers annually, making it the South Asia&rsquo;s largest aviation hub.</p>



                    <p>According to Airports Council International ranking, Indira Gandhi International Airport is routinely one of the busiest airports in the world. Indira Gandhi International Airport is the major hub of various airlines including Air India, Alliance Air, FedEx Express, Quikjet Airlines, and Vistara.</p>



                    <p>To ease the air traffic of Indira Gandhi International Airport, the Ministry of Civil Aviation and the Government of Uttar Pradesh has made the announcement of a new international airport - &lsquo;Noida International Airport&rsquo; in Jewar Uttar Pradesh, which would be operational by April 2025.</p>



