                    <h3>Overview of Jewar Airport </h3>

                    <p>Noida International Airport, also referred to as Jewar Airport, is expected to surpass all other airports in India. Its first phase will span 1,334 hectares and include a 101,590 m&sup2; terminal, a 3,900 m runway, as well as amenities like cargo spaces, a 40-acre maintenance centre, a 37,000 m&sup2; fuel farm, and 167 acres of real estate development.</p>

                    <p>To increase capacity to 30 million passengers a year, Jewar Airport&rsquo;s Phase II will include a second terminal and runway. When it is finished, Jewar International Airport will have 489,700 aircraft movements, 186 parking stands, a 4,150 m runway, a 386,410 m&sup2; terminal, eight fuel tanks, and a multimodal cargo hub, all able to accommodate 70 million people, hence establishing Jewar, Uttar Pradesh, as a major real estate hub.</p>

