                    <h2 id="heading-1">Timeline of Jewar Airport Latest News</h2>



                    <p><strong>2001:</strong> The project was proposed as &lsquo;Taj International Aviation Hub&rsquo; by the then Chief Minister of Uttar Pradesh, Rajnath Singh.</p>



                    <p><strong>2012:</strong> The project to build an international airport in Jewar was shelved by the then Chief Minister of Uttar Pradesh, Akhilesh Yadav as he planned a new airport in Tundla of the Firozabad District.</p>



                    <p><strong>2018:</strong> The project was revived by the BJP Government and allocated ₹1,260 Cr to build an international airport in Jewar Uttar Pradesh under the Public-Private Partnership (PPP) Model.</p>



                    <p><strong>2019: </strong>The process of land acquisition for Phase 1 of the project commenced.</p>



                    <p><strong>2020:</strong> The contract to develop and operate the Jewar Airport for 40 years was awarded to Zurich Airport International AG. The authority was also given a tender to construct a 760 m road that connects the Jewar Airport to the Yamuna Expressway.</p>



                    <p><strong>2021:</strong> The master plan of the Jewar Airport was approved by the Airport Authority of India (AAI). The same year Prime Minister Narendra Modi laid the foundation stone of the airport.</p>



                    <p><strong>2022:</strong> The contract for civil construction work was awarded to Tata Projects Limited.</p>



                    <p><strong>2023:</strong> International Air Transport Association (IATA) awarded the airport code &lsquo;DXN&rsquo; to the Jewar Airport.&nbsp;</p>



                    <p><strong>2024:</strong> Noida International Airport Limited started conducting calibration tests in Jewar Airport, with air calibration of DVOR and DME successfully completed under the guidance of AAI.</p>



                    <h2 id="heading-2">Jewar Airport - Brief Description on the upcoming international airport in UP</h2>



                    <p>Noida International Airport, also known as Jewar Airport, is the upcoming airport in Jewar Uttar Pradesh being developed by Yamuna International Airport Private Limited, a 100% subsidiary of Zurich Airport International AG.</p>



                    <p>This upcoming Noida International Airport Jewar is planned to be India&rsquo;s and Asia&rsquo;s largest international airport. Initially, the proposed plan is to build a 2-runway airport by April 2025 and expand it into a staggering 7,200 acres 8-runway international airport with the aim to handle about 1.2 Cr (12 million) passengers annually. Jewar Airport would be the 3rd commercial airport in the NCR along with Indira Gandhi International Airport and Hindon Airport.</p>



                    <p>Jewar Airport is proposed to be operational from April 2025 and connect cities like Agra, Mathura, Gautam Buddha Nagar etc. to the world&#39;s aviation network through a dynamic blend of Swiss efficiency and Indian hospitality.&nbsp;</p>

                    <p>This upcoming international airport in UP features an array of world-class services and facilities for a seamless passenger experience, and enhanced connectivity via the Yamuna Expressway and other wide ranges of roads, highways, and railways.</p>

