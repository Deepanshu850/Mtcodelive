                    <h3>Difference between Highway and Expressway </h3>

                    <p>Expressways refers to controlled-access highways and offers high-speed roads with limited entry and exit points, controlled by the use of cloverleaf, three-way trumpet, or grade separated to allow a faster and seamless travel.</p>

                    <p>Highways on the other hand have open-access roads along with multiple traffic points and varying speed limits. Expressways feature various mixes of signals and intersections.</p>

                    <h3>Greenfield Expressway </h3>

                    <p>Greenfield Expressway refers to a 12-lane wide expressway in India with initial construction of 8 lanes with maximum speed limit set to 120 km/h for all types of vehicles. The land for further 4 lanes is reserved at the centre of the expressways for future construction. The goal of greenfield expressways is to cut costs on land acquisition and reduce logistics costs. Delhi - Mumbai Expressway is one of the examples of a greenfield expressway with initial 8-lane construction expandable up to 12-lane.</p>

